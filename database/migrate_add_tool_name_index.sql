-- SQLite 版本（SQLite 不支持 ALTER TABLE ADD INDEX，需要重建表）
-- 由于 SQLite 的限制，建议在应用层处理，或使用以下流程：
-- 1. 创建新表 tools_new
-- 2. 复制数据
-- 3. 删除旧表
-- 4. 重命名新表
-- 5. 创建索引

-- =====================================================
-- 2. 验证索引是否创建成功
-- =====================================================

-- MySQL 验证
SHOW INDEX FROM tools WHERE Key_name = 'idx_tool_name';

-- SQLite 验证
-- .schema tools

-- =====================================================
-- 3. 分析表以优化查询计划
-- =====================================================

ANALYZE TABLE tools;

